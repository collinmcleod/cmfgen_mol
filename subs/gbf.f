C
C Function to compute the bound-free gaunt factors for
C a hydrogenic ion of charge Z. The method uses approximate
C formulae which have been fitted to the results of Karasz
C and Latter by Mihalas (Ap. J. ,149,1967, p171) .
C For n> 10 the bound free coefficients are set to unity.
C The frequency should be given in units of 1.0E+15 Hz and the
C temperature in units of 1.0E+04 K .
C
	FUNCTION GBF(RNU,N,ZHE)
	IMPLICIT NONE
C
C Altered 24-May-1996 : IMPLICIT NONE installed.
C                       A now declared REAL*8
C
	INTEGER*4 N
	REAL*8 GBF,RNU,ZHE
C
	REAL*8 A(0:6,10),T1
	DATA A
	1/1.2302628,-2.9094219E-03,7.3993579E-06,-8.7356966E-09
	1,-5.5759888,12.803223,0.0
	1,1.1595421,-2.0735860E-03,2.7033384E-06,0.0
	1,-1.2709045,2.1325684,-2.0244141
	1,1.1450949,-1.9366592E-03,2.3572356E-06,0.0
	1,-0.55936432,0.52471924,-0.23387146
	1,1.1306695,-1.3482273E-03,-4.6949424E-06,2.3548636E-08
	1,-0.31190730,0.1968356,-5.4418565E-02
	1,1.1190904,-1.0401085E-03,-6.9943488E-06,2.8496742E-08
	1,-0.16051018,5.5545091E-02,-8.9182854E-03
	1,1.1168376,-8.9466573E-4,-8.8393113E-06,3.4696768E-08
	1,-0.13075417,4.1921183E-02,-5.5303574E-03
	1,1.1128623,-7.4883260E-04,-1.0244504E-05,3.8595771E-08
	1,-9.5441161E-02,2.3350812E-2,-2.2752881E-03
	1,1.1093137,-6.2619148E-04,-1.1342068E-05,4.1477731E-08
	1,-7.1010560E-02,1.3298411E-02,-9.7200274E-04
	1,1.1078717,-5.4837392E-04,-1.2157943E-05,4.3796716E-08
	1,-5.6056560E-02,8.5139736E-03,-4.9576163E-04
	1,1.1052734,-4.4341570E-04,-1.3235905E-05,4.7003140E-08
	1,-4.7326370E-02,6.1516856E-03,-2.9467046E-04/
C
	T1=RNU/ZHE/ZHE/0.2998		!To convert from microns to hz
	IF(N .GT. 10 .OR. T1 .GT. 110)THEN
	  GBF=1
	ELSE
	  GBF=A(0,N)+(A(1,N)+(A(2,N)+A(3,N)*T1)*T1)*T1
	1  +(A(4,N)+(A(5,N)+A(6,N)/T1)/T1)/T1
	END IF
C
C
	RETURN
	END
